#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: Prints all single digit numbers of base 10
 * starting from 0, followed by a new line.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	putchar('0'); /* Print the digit 0 */
	putchar('\n'); /* Print a newline character */

	return (0);
}

